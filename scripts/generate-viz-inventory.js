// scripts/generate-viz-inventory.js
// Generates docs/viz-architecture.md — the inventory of REUSABLE front-end / build modules, so a
// development cycle can FIND and reuse an existing renderer/helper/generator/module before writing
// a second copy. (The failure this closes: rebuilding `drawRibbon` instead of calling it.)
//
// The inventory is DERIVED FROM THE CODE — do not hand-edit docs/viz-architecture.md. Auto-discovery
// is by convention: scripts/lib/* (shared build helpers), scripts that require ./lib/viz-page (page
// generators), and any module that exports a `window`/`self.SensoryNav*` global (browser/worker
// modules). Add a module by following those conventions; then regenerate:
//   node scripts/generate-viz-inventory.js
"use strict";
const fs = require("fs");
const path = require("path");
const ROOT = path.join(__dirname, "..");

// The file's own purpose, from its leading `//` comment block (filename prefix stripped).
function leadComment(src, base) {
  const lines = src.split(/\r?\n/), out = [];
  for (const l of lines) {
    if (l.trim() === '"use strict";' || l.trim() === "") { if (out.length) break; else continue; }
    const m = l.match(/^\s*\/\/ ?(.*)$/);
    if (m) out.push(m[1]); else break;
  }
  let text = out.join(" ").trim();
  text = text.replace(new RegExp("^" + base.replace(/[.]/g, "\\$&") + "\\s*[—-]?\\s*"), ""); // drop leading filename
  text = text.replace(/^(scripts|harness|recorder|lib)\/\S+\.\w+\s*[—-]?\s*/, "");
  return text.length > 240 ? text.slice(0, 237).replace(/\s+\S*$/, "") + "…" : text;
}

// Exported entry points: module.exports = {…}, or a `const exported|api = {…}` export block.
function entryPoints(src) {
  const names = new Set();
  const grab = (obj) => obj.split(",").forEach((p) => { const n = p.split(":")[0].trim(); if (/^[A-Za-z_$][\w$]*$/.test(n)) names.add(n); });
  let m = src.match(/module\.exports\s*=\s*\{([^}]*)\}/); if (m) grab(m[1]);
  m = src.match(/const\s+(?:exported|api)\s*=\s*\{([^}]*)\}/); if (m) grab(m[1]);
  return Array.from(names);
}

function info(rel) {
  const src = fs.readFileSync(path.join(ROOT, rel), "utf8");
  const ns = (src.match(/(?:window|self)\.(SensoryNav\w+)\s*=/) || [])[1] || null; // the EXPORTED global
  return { rel, desc: leadComment(src, path.basename(rel)), exports: entryPoints(src), ns };
}

// Page-specific orchestrators/workers that assign a global but are NOT reusable modules — they
// consume the inventory (listed under "Not in this inventory"), so keep them out of the module list.
const SKIP_ROOT = new Set(["app.js", "theme.js", "capture.js", "analyze.js", "analyze-worker.js"]);

// ---- discover the reusable layer by convention ----
const shell = fs.readdirSync(path.join(ROOT, "scripts", "lib")).sort().map((f) => info("scripts/lib/" + f));
const generators = fs.readdirSync(path.join(ROOT, "scripts")).filter((f) => f.endsWith(".js"))
  .filter((f) => /require\(["']\.\/lib\/viz-page["']\)/.test(fs.readFileSync(path.join(ROOT, "scripts", f), "utf8")))
  .sort().map((f) => info("scripts/" + f));
const modules = [];
for (const dir of [".", "recorder", "harness/audio", "harness/score", "harness/motion", "harness/tags"]) {
  const abs = path.join(ROOT, dir);
  if (!fs.existsSync(abs)) continue;
  for (const f of fs.readdirSync(abs).filter((f) => f.endsWith(".js")).sort()) {
    if (dir === "." && SKIP_ROOT.has(f)) continue;
    const rel = dir === "." ? f : dir + "/" + f;
    if (/(?:window|self)\.SensoryNav\w+\s*=/.test(fs.readFileSync(path.join(ROOT, rel), "utf8"))) modules.push(info(rel));
  }
}

// ---- render the doc ----
const row = (i) => "| `" + i.rel + "` | " + (i.desc || "—") + " | " + (i.exports.length ? i.exports.map((e) => "`" + e + "`").join(", ") : "—") + (i.ns ? " · `" + i.ns + "`" : "") + " |";
const section = (title, items, cols) => "### " + title + "\n\n| Module | What it is / when to use | " + cols + " |\n|---|---|---|\n" + items.map(row).join("\n") + "\n";

const doc = `<!-- GENERATED by scripts/generate-viz-inventory.js — do not hand-edit. Regenerate after adding a reusable module. -->
# Reusable front-end / build inventory

**TL;DR:** Before you build a page, renderer, generator, shared helper, or browser/worker module,
**check this list and reuse or extend what's here** instead of writing a second copy. This is the
gate that would have caught rebuilding \`drawRibbon\` instead of calling it. The list is generated
from the code (\`scripts/generate-viz-inventory.js\`) by convention — \`scripts/lib/*\`, page
generators that use \`viz-page\`, and any module exporting a \`window\`/\`self.SensoryNav*\` global.

## Contents
- [Shared build helpers](#shared-build-helpers)
- [Page / artifact generators](#page--artifact-generators)
- [Browser / worker modules](#browser--worker-modules)
- [How to reuse](#how-to-reuse)
- [Not in this inventory](#not-in-this-inventory)

## Shared build helpers

Node-side helpers the generators call. **Reuse these when emitting any page or shared asset.**

${section("scripts/lib", shell, "Entry points").replace(/^### .*\n\n/, "")}
## Page / artifact generators

Scripts that emit a data-driven page (small shell that fetches JSON and renders client-side — see
[[data-driven-artifacts]]). **Extend one of these before writing a new page generator.**

${section("generators", generators, "Entry points").replace(/^### .*\n\n/, "")}
## Browser / worker modules

Modules that attach to a \`window\`/\`self.SensoryNav*\` global, so they run in a page, a Worker, AND
Node (dual export). **Load and call these from a page/worker instead of reimplementing the logic** —
e.g. \`window.SensoryNavRibbon.drawRibbon\` is the ONE ribbon renderer; \`SensoryNavScore\` carries the
decode + scoring pipeline; \`SensoryNavCore\` the recorder pieces.

${section("modules", modules, "Exports · global").replace(/^### .*\n\n/, "")}
## How to reuse

1. Find the entry point above (a \`SensoryNav*\` global, a \`buildPage\`/\`drawRibbon\`, a generator).
2. In a browser page/worker: \`<script src="…">\` or \`importScripts("…")\` the module, then call its
   global. In Node/a generator: \`require()\` it.
3. If the existing piece is *close but not identical*, **extract the shared part** into a module here
   and have both callers use it — do not fork a second copy.
4. Adding a reusable module? Follow the conventions (place shared helpers in \`scripts/lib/\`; export a
   \`window\`/\`self.SensoryNav*\` global for browser/worker code), then run
   \`node scripts/generate-viz-inventory.js\`.

## Not in this inventory

- **Theme / dark mode** (\`theme.js\`, \`styles.css\`) — governed by its own gate; see
  [\`docs/dark-mode.md\`](dark-mode.md). Reuse that shared pattern, don't reinvent per page.
- App orchestrators that are page-specific and not meant for reuse (e.g. \`capture.js\`, \`analyze.js\`,
  \`app.js\`) — they *consume* the modules above; they are not themselves reusable modules.
`;

fs.writeFileSync(path.join(ROOT, "docs", "viz-architecture.md"), doc);
console.log("wrote docs/viz-architecture.md —", shell.length, "helpers,", generators.length, "generators,", modules.length, "browser/worker modules");
